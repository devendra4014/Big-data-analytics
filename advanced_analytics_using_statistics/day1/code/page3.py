# broadcast operations

import numpy as np


def function1():
    # math operators
    a1 = np.array([10, 20, 30, 40, 50])

    print(f"a1 + 100 = {a1 + 100}")
    print(f"a1 - 100 = {a1 - 100}")
    print(f"a1 * 100 = {a1 * 100}")
    print(f"a1 / 100 = {a1 / 100}")
    print(f"a1 // 100 = {a1 // 100}")
    print(f"a1 ** 2 = {a1 ** 2}")


# function1()


def function2():
    # comparison operators
    a1 = np.array([10, 20, 30, 40, 50])
    print(f"a1 > 30 = {a1 > 30}")
    print(f"a1 >= 30 = {a1 >= 30}")
    print(f"a1 < 30 = {a1 < 30}")
    print(f"a1 <= 30 = {a1 <= 30}")
    print(f"a1 == 30 = {a1 == 30}")
    print(f"a1 != 30 = {a1 != 30}")


# function2()


